public void GuardarCliente(ObjectContainer BaseFinal, String tipo_Cliente, String nombreUsuario, String password,
            String cedula, String nombres, String apellidos, String direccion, String correo, String celular, String genero, String fechaNacimiento, String estadoCivil) {

        Cliente cliente = new Cliente( );

        if (VerificarCliente(BaseFinal, nombreUsuario) == 0) {

            BaseFinal.set(cliente);

            JOptionPane.showMessageDialog(this, "Cliente Guardado");

        } else {

            JOptionPane.showMessageDialog(this, "Ya existe el Usuario,Intente otro Usuario");
        }
    }

    public static int VerificarCliente(ObjectContainer BaseFinal, String user) {

        Cliente PBuscar = new Cliente(user);

        ObjectSet resultado = BaseFinal.get(PBuscar);//objeto tipo registro

        return resultado.size();
    }
    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        boolean usuarioRepetido = false;

        Query cliente = BaseFinal.query();
        cliente.constrain(Cliente.class);
        cliente.descend("usuario_Cliente").constrain(txtNombreUser.getText());
        ObjectSet<Cliente> result = cliente.execute();

        if (result.size() > 0) {
            JOptionPane.showMessageDialog(this, "Usuario ya registrado");
            usuarioRepetido = true;
        }

        if (!usuarioRepetido) {

            String auxcedula = txtCedulaCliente.getText();
            String auxnombre = txtNombresCliente.getText();
            String auxapellido = txtApellidosCliente.getText();
            String auxdireccion = txtDireccionCliente.getText();
            String auxcorreo = txtCorreoCliente.getText();
            String auxcelular = txtCelularCliente.getText();
            String auxfechanacimiento = txtFechaNacimiento.getText();
            String auxestadocivil = (String) comboxEstadoCivil.getSelectedItem();
            String auxgenero = (String) comboxGenero.getSelectedItem();
            String auxtipocliente = (String) comboxTipoCliente.getSelectedItem();
            String auxnombreuser = txtNombreUser.getText();
            String auxpassuser = txtPassUser.getText();

            Cliente cliente1 = new Cliente();

            cliente1.setCedula(auxcedula);
            cliente1.setNombres(auxnombre);
            cliente1.setApellidos(auxapellido);
            cliente1.setDireccion(auxdireccion);
            cliente1.setCorreo(auxcorreo);
            cliente1.setCelular(auxcelular);
            cliente1.setFechaNacimiento(auxfechanacimiento);
            cliente1.setEstadoCivil(auxestadocivil);
            cliente1.setGenero(auxgenero);
            cliente1.setTipo_Cliente(auxtipocliente);
            cliente1.setUsuario(auxnombreuser);
            cliente1.setPassword(auxpassuser);
            
            //agrega a la base de datos
            
            GuardarCliente(BaseFinal, auxtipocliente, auxnombreuser, auxpassuser, auxcedula, auxnombre, auxapellido, auxdireccion,
                    auxcorreo, auxcelular, auxgenero, auxfechanacimiento, auxestadocivil);
        }

        
        txtCedulaCliente.setText("");
        txtNombresCliente.setText("");
        txtApellidosCliente.setText("");
        txtDireccionCliente.setText("");
        txtCorreoCliente.setText("");
        txtCelularCliente.setText("");
        txtFechaNacimiento.setText("");
        comboxEstadoCivil.setSelectedIndex(-1);
        comboxGenero.setSelectedIndex(-1);
        comboxTipoCliente.setSelectedIndex(-1);
        txtNombreUser.setText("");
        txtPassUser.setText("");
    }//GEN-LAST:event_btnRegistrarActionPerformed

